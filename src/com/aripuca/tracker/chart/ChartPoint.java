package com.aripuca.tracker.chart;

public class ChartPoint {

	private float valueX;
	private float valueY;

	public ChartPoint(float x, float y) {
		valueX = x;
		valueY = y;
	}

	public float getValueX() {
		return valueX;
	}

	public float getValueY() {
		return valueY;
	}

}
